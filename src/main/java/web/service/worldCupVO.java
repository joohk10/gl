package web.service;

public class worldCupVO {

	private String worldCupSeq = "";
	private String hostMemSeq = "";
	private String memSeq = "";
	private String logDate = "";
	private String cnt = "";
	private String chk = "";
	
	public String getWorldCupSeq() {
		return worldCupSeq;
	}
	public void setWorldCupSeq(String worldCupSeq) {
		this.worldCupSeq = worldCupSeq;
	}
	public String getHostMemSeq() {
		return hostMemSeq;
	}
	public void setHostMemSeq(String hostMemSeq) {
		this.hostMemSeq = hostMemSeq;
	}
	public String getLogDate() {
		return logDate;
	}
	public void setLogDate(String logDate) {
		this.logDate = logDate;
	}
	public String getCnt() {
		return cnt;
	}
	public void setCnt(String cnt) {
		this.cnt = cnt;
	}
	public String getChk() {
		return chk;
	}
	public void setChk(String chk) {
		this.chk = chk;
	}
	public String getMemSeq() {
		return memSeq;
	}
	public void setMemSeq(String memSeq) {
		this.memSeq = memSeq;
	}
	
}
