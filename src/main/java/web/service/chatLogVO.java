package web.service;

public class chatLogVO {
	
	private String chatLogSeq = "";
	private String chatRoomSeq = "";
	private String chatMemSeq = "";
	private String chatMsg = "";
	private String chatTime = "";
	private String memName = "";
	
	public String getChatLogSeq() {
		return chatLogSeq;
	}
	public void setChatLogSeq(String chatLogSeq) {
		this.chatLogSeq = chatLogSeq;
	}
	public String getChatRoomSeq() {
		return chatRoomSeq;
	}
	public void setChatRoomSeq(String chatRoomSeq) {
		this.chatRoomSeq = chatRoomSeq;
	}
	public String getChatMemSeq() {
		return chatMemSeq;
	}
	public void setChatMemSeq(String chatMemSeq) {
		this.chatMemSeq = chatMemSeq;
	}
	public String getChatMsg() {
		return chatMsg;
	}
	public void setChatMsg(String chatMsg) {
		this.chatMsg = chatMsg;
	}
	public String getChatTime() {
		return chatTime;
	}
	public void setChatTime(String chatTime) {
		this.chatTime = chatTime;
	}
	public String getMemName() {
		return memName;
	}
	public void setMemName(String memName) {
		this.memName = memName;
	}
	
	
	
}
